class leap_year
{
public static void main (String args[])
{
int a=2013,b=2016;
if(a%4==0)
System.out.println("The year "+a+" is a  leap year");
else
System.out.println("The year "+a+" is not a leap year");
if(b%4==0)
System.out.println("The year "+b+" is a  leap year");
else
System.out.println("The year "+b+" is not a leap year");
}
}